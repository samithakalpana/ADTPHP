<p class="align_left">The Simon Hewavitarne Trust (known as the S. A. Hewavitarne Trust) was set up by Simon, a younger brother of Anagarika Dharmapala, prior to his untimely death. He bequeathed all his wealth to Buddhist scholarship activities, which included making the Tipitaka (the triple basket of Buddhist scriptures) available to the public and temples, building “awasa” (halls of residence for monks) and distributing grants for meritorious work and outstanding scholarship of the Dhamma. Modern scholars acknowledge that the contribution made by his bequest to translate the Pali canon into Sinhala is immeasurable.
</p>
<p>In executing Simon’s will, his brother Charles, brother-in-law Jacob Moonesinghe and nephew Kumaradasa Moonesinghe, who were trustees at the Vidyodaya Privena, spearheaded the publications of the Pali commentaries into Sinhala. These included Paramattha Dípaní or The Commentary of the Petavatthu, edited by Siri Dhammáráma Tissa Náyaka Théra and Mápalagama Chandajóti Théra, assistant to the principal of the Vidyodaya Oriental College, Colombo;  the Visuddhi-Magha edited by Pamunuwe  Buddhadatta Thera, vice principal of the Sirisaddhammodaya Pirivena, Panadura, making them available in print for the first time.
</p>

