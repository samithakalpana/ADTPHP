							<p class="designation">(1846 – 27 July 1936)</p>
							<p><b>Mallika</b>, Anagarika Dharmapala’s mother, was the matriarch of the Family. She grew up in an atmosphere of devotion to Buddhism and its cause. Her father, Andiris Perera Dharmagunawardene, one of the wealthiest Buddhists in Colombo, was a pioneer in the movement for the revival of Buddhism.  He donated an extensive block of land at Maligakanda in Colombo's Maradana area and the funds to establish the first monastic college for Buddhist monks, which became known as the Vidyodaya Oriental College, under the stewardship of the erudite scholar-monk of that era, the Venerable Hikkaduwe Sri Sumangala nahimi (thero).

							</p>
							<p>	While born into considerable wealth, Mallika lived her life with detachment and an ethos of renunciation. Anagarika Dharmapala writes in his diary that while the seed of Bodhi was established by his father, it was his mother who “used to preach to [him] the doctrine of impermanence weekly….”. Her detachment, Angarika Dharmapala came to believe, was the work of the devas of light who wanted to prevent him from being attached to and tied down by family.						
							

