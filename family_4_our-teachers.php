				<p>“It was on my Achchi's (grandmother's) knee that I first heard of the 1915 riots. To my young ears, it was just another of grandma's tales. The significance of those turbulent times dawned on me only later, when as a youthful law student I came across the proceedings of the Field General Court Martial of the British Raj, which probed those riots.


	</p><p>Achchi's husband - my grandfather - had been a teenager when his own father Edmund Hewavitarne (younger brother of the Anagarika Dharmapala) along with Namanidewage Albert Wijesekera was hauled up before the drumhead Court Martial presided over by Lt. Colonel R.L. Muspratt-Williams of the Royal Garrison Artillery and charged with treason and shop-breaking.

	</p><p>The normal laws had been suspended. Civilians were tried on the orders of Governor Robert Chalmers under the Army Act. Charge 1 read: "The above-named accused (Hewavitarne and Wijesekera) are charged with treason, in that they, at Colombo, on or about the 1st June, 1915, did levy war against Our Lord the King, contrary to Section 41 of the Army Act."

	</p>
	<p>The riots, of course, were a reaction in Colombo to what had just happened in and around Gampola where the island's Sinhalese and Muslims had sharp disagreements over the use of loudspeakers outside mosques.
							</p><p>
							A mob of Sinhalese had broken into and looted a Muslim-owned shop, "Crystal Palace" down Keyzer Street in the Pettah. Hewavitarne and Wijesekera were accused of leading that mob.
							</p><p>The Court Martial was held the very next month. The Attorney General appeared for the British Crown. Two legal luminaries of the time, Frederick Norton and Allan Drieberg led the defence team. After many witnesses were heard on both sides and within just three days of trial, the accused were found guilty of treason.

	</p><p>The charge of shop-breaking was dropped, and the accused sentenced to ''penal servitude for life''. The death of my grandfather at the age of 42,1eaving my grandmother a widow at 31,was traced to the traumatic experience he would have undergone at that tender age, seeing his father incarcerated first at the Welikada Jail, later in Jaffna - and then succumb to the deadly disease of the tropics at the time, enteric fever contracted in prison. The Jaffna Prison 'hospital' was just another cell. The patient lay on a mat, on the floor, with no attention and no suitable treatment. When recovery was hopeless and only then, was the patient transferred to the civil hospital in Jaffna.

	</p><p>Five days before his death, his brother Dr. C.A. Hewavitarne was permitted, after having to petition the Colonial Secretary, to attend to his dying brother. But it was too late.
Edmund Hewavitarne died on November 19, 1915, five months after he was sentenced by an army officer to a life of "rigorous imprisonment". A large gathering honoured a patriot, not a traitor, at his funeral.



	</p><p>A 13-page petition of appeal by his widow Sujata Hewavitarne together with petitions and affidavits by leading Buddhist monks and lay persons from different communities, and by Mallika Hewavitarne (mother of Edmund and the Anagarika) was sent to London addressed to the Secretary of State for the Colonies, Andrew Bonar Law asking that Edmund Hewavitarne's name be cleared. The petitions were upheld. Sir Robert Chalmers KCB the Governor and Commander-in-Chief in and over the lsland of Ceylon was recalled to Britain for his mishandling of the riots.

	</p><p>The new Governor, Sir Henry William Manning apologised on behalf of the British Empire to Mrs. Edmund Hewavitarne for the miscarriage of British justice. He and Lady Manning became personal friends of the Hewavitarne family.”

