				<p>
	Bhikkhu Sangarakshita writes:
…At the end of the year [11th of November 1931] he had the satisfaction of seeing the opening ceremony of the Mulagandhakuti Vihāra [in Sarnath] set as it were as a crown upon his labours. The celebrations lasted for three days, during which there were accommodated in Sarnath nearly one thousand visitors, more than half of whom had arrived from overseas. In the afternoon of November 11th, when the sun shone from a clear blue winter sky on the yellow robes of the Bhikkhus and the brilliant silks of the assembled devotees, a golden casket containing Sacred Relics of the Lord Buddha was presented by the Director General of Archaeology to the Mahā Bodhi Society on behalf of the Government of India. Amidst great jubilation the Sacred Relics were placed on the back of an elephant, and taken in a colourful procession which circumambulated the Vihāra thrice. In his address at the opening ceremony of the Vihāra Ven. Dhammapāla recalled his first visit to Sarnath in January 1891 when, he said, it was “in the occupation of low-class hog breeders.” The vast audience was silent as he recounted some of the difficulties he had faced, and it was with a thrill of triumph in his voice that the old warrior, who had been wheeled to the pandal in an invalid-chair, declared, “After an exile of eight hundred years the Buddhists have returned to their own dear Holy Isipatana. It is the wish of the Mahā Bodhi Society,” he said in conclusion, “to give to the people of India without distinction of caste and creed the compassionate doctrine of the Samma Sambuddha. I trust that you would come forward to disseminate the Arya Dhamma of the Tathāgata throughout India.” (from Sangharakshita, B (1952), Angarika Dharmapala: A Biographical Sketch, Buddhist Publications Society).

	</p><p>
	In India, his health continued to deteriorate, even though his brave and determined spirit sored as always, attempting to bring to fulfilment new projects. However, as he weakened and he recognized that his days were numbered, he obtained higher ordination (Upasampada) in 1933 in Sarnath. Ten monks from Ceylon travelled to India for the ordination. The senior monks admitted him into higher ordination dispensing with the usual requirement of the stipulated time in novice monkhood, taking into account his immeasurable service to the Buddha’s dispensation.
On the 29th of April 1933, surrounded by Buddhist monks chanting Buddhist stanzas (pirith) and in the presence of his disciple Devapriya Valisinha, his nephew, Raja Hewavitarne who had travelled from Ceylon, his physician, Dr Nandy, and the samaneras (novice-monks) who had been sent to Buddha Gaya, Ven. Sri Devamitta Dhammapla’s life came to an end. His face was turned towards the Mulaghandakuti and the last word to leave his lips was “devamitta” (beloved friend of the devas).

	</p><p>
	Two weeks later, his ashes were carried from Sarnath by 
	<a href="benefactors_7_our-teachers.php"><b>Devapriya Valisinha</b></a> to the Mahabodhi Society headquarters in Calcutta (now Kolkota). A portion was interred later in a small stupa at the Mulaghandakuti premises in Sarnath and a portion carried to Ceylon by Valisinha, first by train to Rameshwaran, at the southern tip of India, then by ferry across to Talaimannar in Ceylon, and again by train to Colombo.
	</p><p>
	Unprecedented crowds had gathered at the railway station in Colombo, lining the rail track, chanting “sadhu, sadhu, sadhu”. The urn with the ashes was then taken to the Vidyodaya Pirivena, the monastic college founded by the Hewavitarne family, by procession and handed to the monks there. The monks in turn handed it to Sir Baron Jayatilaka (then the Minister of Home Affairs).

							
							</p><p>
							The crowds that came to pay homage to the ashes had been so vast that a boundary wall of the Privena had to be demolished to make room for the throng of people.
There is a commonly held mis-conception that the part of Anagarika Dharmapala’s ashes that were brought to Sri Lanka are interred in the Hewavitarne family crypt at the General Cemetery in Colombo. This is not correct. The ashes that were brought to Sri Lanka are interred under the statue of Anagarika Dharmapala at the Anagarika Dharmapala Trust headquarters opposite the Pirivena.


