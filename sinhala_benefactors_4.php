							<!-- <h3>Gopal dhanam</h3> -->
							<h3>හෙලේනා පෙට්රොව්නා බ්ලැවට්ස්කි මැතිනිය</h3>
							<!-- <p class="designation">Yoga Teacher</p> -->
							<p>	රුසියානු ජාතික කාන්තාවක් වූ හෙලේනා බ්ලැවට්ස්කි මැතිනිය, පරමවිඥානාර්ථ සංගමයේ ආරම්භක සාමාජිකාවක් වූවාය. ඇය අධ්‍යාර් නගරයේ සිට ශ්‍රී ලංකාවට ඕල්කොට් තුමා සමග පැමිණියාය.  ලංකාවේ දකුණු පළාතේ තොටුපලක් වූ ගාලු තොටෙන් මෙරටට පැමිණි මේ දෙදෙනා එහිදී බුද්ධාගම වැළඳ ගත්හ. ගාලු ප්‍රදේශයේ ප්‍රධාන සංඝනායක ඉදිරිපිටදී දණගසා බුද්ධාගම වැළද ගත් මේ දෙ‍ෙදනා දුටු ගාලු වැසියෝ අතිශයින් ප්‍රීතියට (අමන්දානන්දයට) පත් වූහ. මීට පෙර සුදු ජාතිකයින් හැමවිටම උත්සහ කළේ බෞද්ධාගමට පහර ගැසීමට මිස අගය කිරීමට නොවන බව ඔවුහු දැන සිටියෝය.
							</p>
							<p>	මේ වන විට තරුණ ඩේවිඩ් හේවාවිතාරණ ශාන්ත තෝමස් ක්‍රිස්තියානි විදුහලේ ද්විතියික අධ්‍යාපනය ලබමින් සිටියේය. බ්ලැවට්ස්කි මැතිනියගේ ආගමික දැනුම හා උනන්දුව කෙරෙහි ඩේවිඩ් හේවාවිතාරණගේ සිත තදින්ම ඇදී ගියේ පහන් සිලකට ඇදී යන පළගැටියෙක් පරිද්දෙනි. ඔහු සිය “අතීත ආවර්ජනා” වල සඳහන් කරන්නේ තමා කුඩාකල සිට ආගමික මිත්‍යා විශ්වාස වල ඇති බලයෙන් වශී වූ බවත්, තවුසන්ගේ ජීවිත ගැනත්, රහතුන් වහන්සේලාගේ මානසික බලය ගැනත් තොරතුරු ගවේෂණය කරමින් සිටිය බවය. 

	</p><p>මේ තොරතුරු ඇසුරෙන් පැහැදිලි වන කරුණක් නම් තරුණ දොන් ඩේවිඩ් බ්ලැවට්ස්කි මැතිනියගේ විශ්වාසයන් කෙරෙහි තදින් මානසික වශයෙන් බැඳී සිටිය බවය. ඒ නිසා දොන් ඩේව්ඩ්ගේ පියා වූ දොන් කරෝලිස් හේවාවිතාරණ මහතාගේ කැමැත්ත පරිදි මේ තරුණයාව බ්ලැවට්ස්කි මැතිනිය සමග ඉන්දියාව බලා යෑමට එකඟත්වය ලබා ගැනීමට හැකි විය. අනාගත අනගාරික ධර්‍මපාලතුමා මදුරාසියේ අද්යාර්හි ආගමික විශ්වාසයන් කෙරෙහි ඇදී ගියේ මේ ලෙසිනි.
	</p><p>මේ වන විට වයස අවුරුදු 20 ක් පමණ වූ මේ තරුණයා, එකල සිටි බොහෝ බෞද්ධයින් මෙන් බුද්ධාගම හා පරමවිඥානාර්ථ සංගමයේ අදහස් බොහෝ දුරට සමාන වන බව විශ්වාස කළේය.
	</p><p>
	කලකදී මේ අදහස් යම් යම් වෙනස්වීම් වලට ලක්වූයේ, බ්ලැවට්ස්කි මැතිනියගේ මරණයෙන් පසුව ඕල්කොට් තුමා ඈනී බෙසන්ට් මැතිනියගේ ආගමික අදහස් කෙරෙහි උනන්දුවක් දැක්වීමත් සමගය. ඒ අනුව ඔවුන්ගේ අදහස් වලට හින්දු ආගමික බලපෑම්ද ඇතුලත් විය.
	</p>
	<p>අනගාරික ධර්‍මපාලතුමාගේ මව වූ මල්ලිකා හේවාවිතාරණ හැරුණු විට ධර්‍මපාලතුමාගේ චරිතය කෙරෙහි බෙහෙවින් බලපෑ අනෙක් තැනැත්තිය වූයේ බ්ලැවට්ස්කි මැතිනියයි. තරුණ අනගාරික ධර්‍මපාලතුමාගේ අදහස් කෙරෙහි බ්ලැවට්ස්කි මැතිනියගේ බලපෑම නිසැක වශයෙන්ම ප්‍රබල වූ බව පැහැදිලිය.

